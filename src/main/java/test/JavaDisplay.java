/*8. Write a Java program to display the following pattern. Go to the editor
Sample Pattern :

   J    a   v     v  a
   J   a a   v   v  a a
J  J  aaaaa   V V  aaaaa
 JJ  a     a   V  a     a
 */

public class JavaDisplay {

    public static void main(String[] args) {

        System.out.println("   J    a   v     v  a ");
        System.out.println("   J   a a   v   v  a a");
        System.out.println("J  J  aaaaa   V V  aaaaa");
        System.out.println(" JJ  a     a   V  a     a");

        System.out.println();
        System.out.println();

        System.out.println("   J   U   U   N   N  I   OOOO    RRRR    ");
        System.out.println("   J   U   U   NN  N  I  O    O   R   R ");
        System.out.println("J  J   U   U   N N N  I  O    O   RRR    ");
        System.out.println(" JJ     UUU    N  NN  I   OOOO    R  R  ");
    }
}
